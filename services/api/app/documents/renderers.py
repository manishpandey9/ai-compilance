def render_annex_viii_csv(ctx: RenderContext) -> bytes:
    from app.documents.official_sections import ANNEX_VIII_SECTION_A_FIELDS

    buf = io.StringIO()
    writer = csv.writer(buf)
    writer.writerow(["field_id", "field_label", "value", "legal_basis"])
    basis = "Regulation (EU) 2024/1689 Annex VIII Section A (Art. 49(1))"
    for field_id, label in ANNEX_VIII_SECTION_A_FIELDS:
        value = ""
        if field_id == "1":
            value = ctx.company_name
        elif field_id == "4":
            value = ctx.system_name
        elif field_id == "5":
            from app.documents.labels import format_answer_value

            value = format_answer_value(
                "use_case_category", ctx.answers.get("use_case_category")
            )
        writer.writerow([field_id, label, value, basis])
    return buf.getvalue().encode("utf-8")


def render_evidence_xlsx(ctx: RenderContext) -> bytes:
    wb = Workbook()
    ws = wb.active
    ws.title = "Evidence Tracker"
    ws.append(
        [
            "evidence_id",
            "obligation_id",
            "requirement",
            "legal_citation",
            "artifact",
            "status",
            "owner",
            "notes",
        ]
    )
    artifact_map = {
        "risk_classification_memo": "01_risk_classification_memo.md",
        "annex-iv-technical-documentation": "04_annex_iv_technical_documentation_template.docx",
        "quality-management-system": "05_quality_management_system_template.docx",
        "human-oversight-plan": "06_human_oversight_plan_template.docx",
        "instructions-for-use": "07_instructions_for_use_template.docx",
        "fria-template": "08_fria_template.docx",
        "eu-declaration-of-conformity": "09_eu_declaration_of_conformity_skeleton.docx",
        "eu-database-registration": "10_eu_database_registration_data_pack.csv",
    }
    for i, o in enumerate(ctx.obligations, start=1):
        artifact = artifact_map.get(o.document_type or "", o.document_type or "manual evidence")
        ws.append(
            [
                f"EV-{i:03d}",
                o.obligation_id,
                o.title,
                o.legal_citation,
                artifact,
                "draft",
                o.suggested_owner,
                o.evidence_required or "",
            ]
        )
    buf = io.BytesIO()
    wb.save(buf)
    return buf.getvalue()


def build_zip(files: dict[str, bytes]) -> bytes:
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        for name, data in files.items():
            zf.writestr(name, data)
    return buf.getvalue()


