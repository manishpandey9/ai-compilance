def render_obligation_csv(ctx: RenderContext) -> bytes:
    buf = io.StringIO()
    writer = csv.writer(buf)
    writer.writerow(
        [
            "obligation_id",
            "title",
            "actor_role",
            "risk_tier",
            "legal_citation",
            "legal_summary",
            "mandatory",
            "evidence_required",
            "linked_document",
            "suggested_owner",
            "status",
            "due_date",
            "enforcement_note",
            "source_url",
        ]
    )
    for o in ctx.obligations:
        writer.writerow(
            [
                o.obligation_id,
                o.title,
                ctx.primary_actor_role,
                ctx.risk_tier,
                o.legal_citation,
                o.legal_summary or "",
                "yes" if o.mandatory else "no",
                o.evidence_required or "",
                o.document_type or "",
                o.suggested_owner,
                "open",
                "",
                o.enforcement_note or "",
                o.source_url or "",
            ]
        )
    return buf.getvalue().encode("utf-8")


class _ProcurementPDF(FPDF):
    def footer(self) -> None:
        self.set_y(-15)
        self.set_font("Helvetica", "I", 8)
        self.cell(
            0,
            10,
            f"Assessment {self.assessment_id} | Page {self.page_no()}",
            new_x=XPos.LMARGIN,
            new_y=YPos.NEXT,
            align="C",
        )


def _pdf_line(pdf: FPDF, text: str, height: float = 6, *, align: str = "") -> None:
    pdf.set_x(pdf.l_margin)
    pdf.cell(0, height, text, new_x=XPos.LMARGIN, new_y=YPos.NEXT, align=align)


def _pdf_wrap(pdf: FPDF, text: str, height: float = 5) -> None:
    pdf.set_x(pdf.l_margin)
    pdf.multi_cell(0, height, text, new_x=XPos.LMARGIN, new_y=YPos.NEXT)


def render_procurement_pdf(ctx: RenderContext) -> bytes:
    """Customer-facing procurement summary for security and legal reviewers."""
    data = ctx.template_dict()
    pdf = _ProcurementPDF()
    pdf.assessment_id = ctx.assessment_id  # type: ignore[attr-defined]
    pdf.set_auto_page_break(auto=True, margin=18)
    pdf.add_page()

    pdf.set_font("Helvetica", "B", 18)
    _pdf_line(pdf, "EU AI Act Procurement Summary", 10)
    pdf.set_font("Helvetica", size=11)
    pdf.ln(2)
    _pdf_wrap(pdf, f"{ctx.system_name} | {ctx.company_name}", 6)
    pdf.ln(4)

    pdf.set_font("Helvetica", "B", 12)
    _pdf_line(pdf, "At a glance", 8)
    pdf.set_font("Helvetica", size=10)
    for line in [
        f"Risk tier: {ctx.risk_tier_label}",
        f"Actor role: {ctx.actor_role_label}",
        f"Classification: {ctx.classification_status} (confidence: {ctx.confidence or 'n/a'})",
        f"Assessment ID: {ctx.assessment_id}",
        f"Rule set v{ctx.rule_version} | Sources {ctx.source_version}",
        f"Generated: {ctx.generated_at}",
    ]:
        _pdf_line(pdf, line, 6)
    pdf.ln(4)

    pdf.set_font("Helvetica", "B", 12)
    _pdf_line(pdf, "System profile", 8)
    pdf.set_font("Helvetica", size=10)
    for label, value in ctx.answer_rows:
        _pdf_wrap(pdf, f"{label}: {value}")
    pdf.ln(2)

    pdf.set_font("Helvetica", "B", 12)
    _pdf_line(pdf, "Why this tier", 8)
    pdf.set_font("Helvetica", size=10)
    if ctx.triggered_rules:
        for rule in ctx.triggered_rules:
            pdf.set_font("Helvetica", "B", 10)
            _pdf_wrap(pdf, rule.legal_citation)
            pdf.set_font("Helvetica", size=10)
            _pdf_wrap(pdf, rule.rationale)
            pdf.ln(1)
    else:
        _pdf_wrap(pdf, "No rules triggered in this assessment.")
    pdf.ln(2)

    pdf.set_font("Helvetica", "B", 12)
    _pdf_line(pdf, "Obligations to address", 8)
    pdf.set_font("Helvetica", size=9)
    for o in ctx.obligations[:12]:
        _pdf_wrap(pdf, f"- {o.title} [{o.legal_citation}]")
    if not ctx.obligations:
        _pdf_wrap(pdf, "No obligations mapped. Request expert review.")
    pdf.ln(2)

    pdf.set_font("Helvetica", "B", 12)
    _pdf_line(pdf, "Documents in this pack", 8)
    pdf.set_font("Helvetica", size=10)
    docs = [
        "Risk classification memo (markdown)",
        "AI system card (markdown)",
        "Obligation matrix (CSV)",
        "This procurement summary (PDF)",
    ]
    if ctx.pack_sku == "evidence_pack" and ctx.is_high_risk:
        docs.extend(
            [
                "Annex IV technical documentation (Art. 11 / Annex IV)",
                "Quality management system (Art. 17)",
                "Human oversight plan (Art. 14)",
                "Instructions for use (Art. 13)",
            ]
        )
        if ctx.needs_fria:
            docs.append("FRIA template (Art. 27)")
        if ctx.needs_provider_conformity:
            docs.extend(
                [
                    "EU declaration of conformity (Art. 47 / Annex V)",
                    "EU database registration pack (Art. 49 / Annex VIII)",
                ]
            )
        docs.append("Evidence tracker (XLSX)")
    for d in docs:
        _pdf_wrap(pdf, f"- {d}")
    pdf.ln(2)

    if ctx.missing_variables:
        pdf.set_font("Helvetica", "B", 12)
        _pdf_line(pdf, "Open gaps before customer submission", 8)
        pdf.set_font("Helvetica", size=10)
        for m in ctx.missing_variables:
            _pdf_wrap(pdf, f"- {m} not provided")
        pdf.ln(2)

    source_title = ctx.legal_source_title.replace(" (Artificial Intelligence Act)", "")
    pdf.set_font("Helvetica", "I", 8)
    _pdf_wrap(
        pdf,
        "Not legal advice. Generated from a structured assessment and versioned rule engine. "
        f"Legal source: {source_title}.",
        4,
    )
    return pdf.output()


